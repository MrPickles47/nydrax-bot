// ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
// Evento principal de mensagens
// ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
bot.on("message", async (msg) => {
  const chatId = msg.chat.id;
  const text = msg.text;

  logUser(chatId, text); // loga tudo que o usu√°rio escreve

  if (!text) return;

  // Comando de menu
  if (text === "/menu") {
    sendMainMenu(bot, chatId);
    return;
  }

  // Resposta padr√£o com IA
  bot.sendMessage(chatId, "üß† Processando...");

  try {
    const response = await client.chat.completions.create({
      model: "gpt-4o-mini",
      messages: [
        { role: "system", content: "Voc√™ √© o Nydrax AI Core, o assistente do Lucas." },
        { role: "user", content: text }
      ],
    });

    const aiReply = response.choices[0].message.content;

    logInfo(`Resposta gerada pela IA: ${aiReply}`);

    bot.sendMessage(chatId, aiReply);

  } catch (error) {
    console.error(error);
    logError(error.message);

    bot.sendMessage(chatId, "‚ùå Erro ao contactar o Nydrax AI Core.");
  }
});
